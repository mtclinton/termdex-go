var (
	mux    *http.ServeMux
	server *httptest.Server
)

func setup() func() {
	mux = http.NewServeMux()

	l, err := net.Listen("tcp", "127.0.0.1:8082")
	if err != nil {
		log.Fatal(err)
	}
	server := httptest.NewUnstartedServer(mux)
	server.Listener.Close()
	server.Listener = l
	server.Start()

	return func() {
		server.Close()
	}
}

func TestPokemon(t *testing.T) {

	teardown := setup()
	defer teardown()

	jsonFile, err := os.Open("pokemon_test.json")
	if err != nil {
		log.Println(err)
	}
	defer jsonFile.Close()

	pokemon_bytes, err := ioutil.ReadAll(jsonFile)
	if err != nil {
		log.Println(err)
	}
	var pokemon_test_response PokemonTestStruct
	json.Unmarshal(pokemon_bytes, &pokemon_test_response)

	mux.HandleFunc("/pokemon/1", func(w http.ResponseWriter, r *http.Request) {
		w.Header().Set("Content-Type", "application/json")
		w.WriteHeader(http.StatusOK)
		json.NewEncoder(w).Encode(pokemon_test_response)
	})

	downloader := NewDownloader(3)
	apiData, err := downloader.get("http://localhost:8082/pokemon/1")
	if err != nil {
		log.Println(err)
	}
	if err != nil {
		log.Println(err)
	}
	assert.Equal(t, "bulbasaur", apiData.Name)
}

